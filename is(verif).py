p = input('digite algo: ')
print(p.isnumeric())  #verifica se é número
print(p.isnumeric())  #verifica se é número
print(p.isalpha())    #verifica se é letra
print(p.isalnum())    #verifica se é letra ou número
print(p.isupper())    #verifica se está em maiúsculo
print(p.islower())    #verifica se está em minúsculo
print(p.isspace())    #verifica se é espaço
print(p.istitle())    #verifica se está capitalizada